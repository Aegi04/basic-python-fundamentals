# Examples and reviews
# Loops by For

total_book = 10
print(f'total book {total_book}')

total_books_read = 0
print(f'total books reads {total_books_read}')

print('Mom says : "read all the books!')

for total_books_read in range(1, total_book + 1):
    print(f'Book {total_books_read} has been read')

print(f'So the total of books that have been read is {total_books_read}')
